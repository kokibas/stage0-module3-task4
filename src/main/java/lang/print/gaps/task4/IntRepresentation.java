package lang.print.gaps.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int c ;
        int h;
        int a;
        int r;
        char c1 = 'c';
        char h1 = 'h';
        char a1 = 'a';
        char r1 = 'r';
        c = c1;
        h = h1;
        a = a1;
        r = r1;
        System.out.println(c);
        System.out.println(h);
        System.out.println(a);
        System.out.println(r);
    }
}
